# Let's also count expanded verses
expanded_count = 0
for ref in all_verses:
    if "vv." in ref:
        # Handle like vv. 1-11 or vv. 1, 10-11
        if "-" in ref:
            parts = ref.replace("vv. ", "").split("-")
            if len(parts) == 2:
                try:
                    start = int(parts[0].split(",")[0].strip())
                    end = int(parts[1].strip())
                    expanded_count += (end - start + 1)
                except:
                    expanded_count += 1
        else:
            expanded_count += 1
    elif "v." in ref:
        expanded_count += 1
    elif "-" in ref and ":" in ref:
        # Handle Rom. 5:1-11
        parts = ref.split(":")
        if len(parts) == 2 and "-" in parts[1]:
            verse_range = parts[1].split("-")
            if len(verse_range) == 2:
                try:
                    start = int(verse_range[0].strip())
                    end = int(verse_range[1].strip())
                    expanded_count += (end - start + 1)
                except:
                    expanded_count += 1
        else:
            expanded_count += 1
    elif "," in ref and ":" in ref:
        # Handle Rom. 16:1, 4-5, 16, 20
        base_parts = ref.split(":")
        if len(base_parts) == 2:
            verse_parts = base_parts[1].split(",")
            for vp in verse_parts:
                if "-" in vp:
                    range_parts = vp.strip().split("-")
                    if len(range_parts) == 2:
                        try:
                            start = int(range_parts[0].strip())
                            end = int(range_parts[1].strip())
                            expanded_count += (end - start + 1)
                        except:
                            expanded_count += 1
                else:
                    expanded_count += 1
    else:
        expanded_count += 1

print(f"\nTotal expanded verse count: {expanded_count}")